    private void adicionaDadosTabela() {
        DefaultTableModel model = (DefaultTableModel) jTUsuarios.getModel();
        model.setRowCount(0);
        for (int x = 0; x < usuarios.size(); x++) {
            if (usuarios.get(x).getNome_usuario().toLowerCase().contains(jTFBusca.getText().toLowerCase())) {
                model.addRow(new Object[]{usuarios.get(x).getId_usuario(), usuarios.get(x).getNome_cargo(), usuarios.get(x).getNome_usuario()});
            }
        }
    }

