    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        usuarios.clear();
        try {
            ResultSet rs = con.select("SELECT * FROM usuario JOIN cargo ON usuario.id_cargo=cargo.id_cargo ORDER BY usuario.id_usuario");
            while (rs.next()) {
                if (!rs.getString("usuario.nome_usuario").equals("Visitante")) {
                    usuarios.add(new Usuario(rs.getInt("usuario.id_usuario"), rs.getInt("usuario.id_cargo"), rs.getString("usuario.nome_usuario"), rs.getString("cargo.nome_cargo")));
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "SQLException: " + ex, "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
        }
        adicionaDadosTabela();
    }//GEN-LAST:event_formWindowGainedFocus

    private void jTUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTUsuariosMouseClicked
        int resposta = JOptionPane.showOptionDialog(this, "O que você deseja fazer com o usuário selecionado?", "Estacionamento Universitário", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Alterar", "Remover", "Visualizar Veículos"}, null);
        try {
            int id_usuario = (int) jTUsuarios.getModel().getValueAt(jTUsuarios.getSelectedRow(), 0);
            switch (resposta) {
                case 0:
                    new TelaManipularUsuario(usuarios.get(jTUsuarios.getSelectedRow())).setVisible(true);
                    break;
                case 1:
                    int respostaDelete = JOptionPane.showOptionDialog(this, "Você realmente deseja deletar o usuário selecionado?", "Estacionamento Universitário", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Sim", "Cancelar"}, null);
                    switch (respostaDelete) {
                        case 0:
                            con.insert("DELETE FROM usuario WHERE id_usuario=" + id_usuario);
                            break;
                        case 1:
                            break;
                    }
                    break;
                case 2:
                    new TelaVeiculos(usuarios.get(jTUsuarios.getSelectedRow())).setVisible(true);
                    break;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Não é possível remover o usuário selecionado pois há veículos do mesmo cadastrado. Por favor, remova antes seus veículos.", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTUsuariosMouseClicked

