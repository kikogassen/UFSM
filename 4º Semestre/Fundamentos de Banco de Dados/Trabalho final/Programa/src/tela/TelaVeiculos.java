    private void jTVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTVeiculosMouseClicked
        int resposta = JOptionPane.showOptionDialog(this, "O que você deseja fazer com o veículo selecionado?", "Estacionamento Universitário", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Alterar", "Remover"}, null);
        try {
            String placa_veiculo = (String) jTVeiculos.getValueAt(jTVeiculos.getSelectedRow(), 0);
            switch (resposta){
                case 0:
                    String placa_nova = perguntaPlaca(true);
                    con.insert("UPDATE veiculo SET placa_veiculo='"+placa_nova+"' WHERE placa_veiculo='"+placa_veiculo+"'");
                    break;
                case 1:
                    con.insert("DELETE FROM veiculo WHERE placa_veiculo='"+placa_veiculo+"'");
                    break;
            }
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "SQLException: "+ex, "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTVeiculosMouseClicked

    private void jBAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarActionPerformed
        String placa = perguntaPlaca(false);
        if (placa != null){
            try {
                con.insert("INSERT INTO veiculo (placa_veiculo, id_usuario) VALUES ('"+placa+"', "+usuario.getId_usuario()+")");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Essa placa já está cadastrada", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBAdicionarActionPerformed

    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        DefaultTableModel model = (DefaultTableModel) jTVeiculos.getModel();
        model.setRowCount(0);
        try {
            ResultSet rs = con.select("SELECT * FROM veiculo WHERE id_usuario="+usuario.getId_usuario());
            while (rs.next()){
                model.addRow(new Object[]{rs.getString("placa_veiculo")});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "SQLException: "+ex, "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formWindowGainedFocus

