    private String perguntaPlaca(boolean alteracao){
        String tmp;
        if (alteracao){
            tmp = " nova";
        } else {
            tmp = "";
        }
        while (true){
            String placa;
            placa = JOptionPane.showInputDialog(this, "Informe a"+tmp+" placa do veículo, no padrão XXX-0000", "Estacionamento Universitário - Veículos", JOptionPane.QUESTION_MESSAGE);
            if (placa==null){
                return null;
            }
            placa = placa.toUpperCase();
            if (validaPlaca(placa)){
                return placa;
            }
        }
    }
    
    private boolean validaPlaca(String placa){
        if (placa.length()==8){
            for (int x=0;x<placa.length();x++){
                if (x>=0 && x<=2){
                    if (placa.charAt(x)<'A' || placa.charAt(x)>'Z'){
                        return false;
                    }
                } else if (x==3){
                    if (placa.charAt(x) != '-'){
                        return false;
                    }
                } else if (x>3){
                    if (placa.charAt(x)<'0' || placa.charAt(x)>'9'){
                        return false;
                    }
                }
            }
            return true;
        } else {
            return false;
        }
    }

