    private int getId_estacionamento() throws SQLException {
        ResultSet rs = con.select("SELECT id_estacionamento FROM estacionamento WHERE nome_estacionamento='" + this.estacionamento + "'");
        rs.first();
        return rs.getInt("id_estacionamento");
    }

    private boolean validaPlaca(String placa) {
        if (placa.length() == 8) {
            for (int x = 0; x < placa.length(); x++) {
                if (x >= 0 && x <= 2) {
                    if (placa.charAt(x) < 'A' || placa.charAt(x) > 'Z') {
                        return false;
                    }
                } else if (x == 3) {
                    if (placa.charAt(x) != '-') {
                        return false;
                    }
                } else if (x > 3) {
                    if (placa.charAt(x) < '0' || placa.charAt(x) > '9') {
                        return false;
                    }
                }
            }
            return true;
        } else {
            return false;
        }
    }

