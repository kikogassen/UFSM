    private void jBConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConfirmarActionPerformed
        if (!jFTFPlaca.getText().equals("")) {
            if (!jTFId.getText().equals("")) {
                try {
                    String placa_veiculo = jFTFPlaca.getText().toUpperCase();
                    if (validaPlaca(placa_veiculo)) {
                        int id_usuario = Integer.parseInt(jTFId.getText());
                        ResultSet rs = con.select("SELECT COUNT(*) AS count FROM veiculo WHERE placa_veiculo='" + placa_veiculo + "' AND id_usuario='" + id_usuario + "'");
                        rs.first();
                        if (rs.getInt("count") == 0) {
                            JOptionPane.showMessageDialog(this, "Este veículo não é deste usuário", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
                            jTFId.setText("");
                            jTFId.requestFocus();
                            return;
                        }
                        rs = con.select("SELECT COUNT(*) AS count FROM estacionada e JOIN veiculo v ON e.placa_veiculo=v.placa_veiculo WHERE v.id_usuario=" + id_usuario + " AND e.datahora_saida_estacionada IS NULL");
                        rs.first();
                        if (rs.getInt("count") >= 1) {
                            JOptionPane.showMessageDialog(this, "Já há veículo(s) deste usuário nos estacionamentos da universidade", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
                            jTFId.setText("");
                            jFTFPlaca.setText("");
                            jFTFPlaca.requestFocus();
                            return;
                        }
                        int id_estacionamento = getId_estacionamento();
                        con.insert("INSERT INTO estacionada (placa_veiculo, id_estacionamento) VALUES ('" + placa_veiculo + "', " + id_estacionamento + ")");
                        JOptionPane.showMessageDialog(this, placa_veiculo + " entrou no estacionamento", "Estacionamento Universitário", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Placa inválida", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "SQLException: " + ex, "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Identificador inválido", "Estacionamento Universitário", JOptionPane.ERROR_MESSAGE);
                }
                jTFId.setText("");
                jFTFPlaca.setText("");
                jFTFPlaca.requestFocus();
            } else {
                jTFId.requestFocus();
            }
        } else {
            jFTFPlaca.requestFocus();
        }
    }//GEN-LAST:event_jBConfirmarActionPerformed

